package de.solti.pathutils.path;

public class TimeOutOfBoundsException extends Exception {
	private static final long serialVersionUID = 1207832267926951732L;

	public TimeOutOfBoundsException() {
		super();
	}

	public TimeOutOfBoundsException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TimeOutOfBoundsException(String message, Throwable cause) {
		super(message, cause);
	}

	public TimeOutOfBoundsException(String message) {
		super(message);
	}

	public TimeOutOfBoundsException(Throwable cause) {
		super(cause);
	}
	
	

}
